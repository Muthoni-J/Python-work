def add(a, b):
    answer = a + b
    return answer

def subtract(a,x):
    result = a- x 
    return result

def multiply(x, y):
    total = x * y
    return total

def modul(d,f):
    mode = d % f
    return mode

def divide(r,x):
    div = r / x
    return div

def exponet(a,x):
    ttal = a ** x
    return ttal

def whole_num(u, t):
    whole = u//t
    return whole

def square(a):
    sqr =  a*a
    return sqr

def cube(b):
    cuboid = b * b * b
    return cuboid